function drawText(x,y,text,width,height) {
    var textthings = [];
    // Matches each letter to a number (0-9 for 1 through 0 and 10-35 for a-z) and sends it to textthings.
    for(var i = 0; i<text.length; i++){
        for(var n = 0; n<letterNum.length-1; n++){
            if(text[i] === letterNum[n]){
                textthings.push(n);
            }
        }
    }
    // Goes through the numbers in textthings and draws the letter using the corresponding letter in letters.
    for(var numberfortext = 0;numberfortext<textthings.length; numberfortext++){
        for(var xtext = 0;  xtext<fontBaseSize; xtext++){
            for(var ytext = 0; ytext<fontBaseSize; ytext++){
                //console.log(letters[textthings[numberfortext]][xtext][ytext]);
                if(letters[textthings[numberfortext]][ytext][xtext]=== "1"){
                    drawRect((x+((fontBaseSize+1)*numberfortext)+xtext)*width,(y+ytext)*height,width,height);
                }
                
            }
        }
    }
}